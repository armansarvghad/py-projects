def print_ascii_art(ascii_art):
    for line in ascii_art:
        print(line)

# Example ASCII art
ascii_art = [
    "  ____                   _   _            _     ",
    " / ___| ___   ___   __ _| |_(_) ___ _ __ | |_   ",
    "| |  _ / _ \ / _ \ / _` | __| |/ _ \ '_ \| __|  ",
    "| |_| | (_) | (_) | (_| | |_| |  __/ | | | |_   ",
    " \____|\___/ \___/ \__,_|\__|_|\___|_| |_|\__|  "
]

# Print the ASCII art
print_ascii_art(ascii_art)
